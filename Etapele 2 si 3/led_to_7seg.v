module led_to_7seg #(
parameter WIDTH   = 'd48 
)(
	input		   clk_i,
	input		   rst_n,
	input [2:0] column_i,
	input       row_i   ,
	output reg [WIDTH-1:0] seg7_pins
);

always @(posedge clk_i or negedge rst_n) begin
	if(~rst_n)
		seg7_pins<=48'b10011100_11111111_11111111_11111111_11111111_11111111;  
		else
	case(row_i) 
		1'b1: begin
			case(column_i)
				3'd0: seg7_pins<=48'b11111111_11111111_11111111_11111111_11111111_10011100;
				3'd1: seg7_pins<=48'b11111111_11111111_11111111_11111111_10011100_11111111;
				3'd2: seg7_pins<=48'b11111111_11111111_11111111_10011100_11111111_11111111;
				3'd3: seg7_pins<=48'b11111111_11111111_10011100_11111111_11111111_11111111;
				3'd4: seg7_pins<=48'b11111111_10011100_11111111_11111111_11111111_11111111;
				3'd5: seg7_pins<=48'b10011100_11111111_11111111_11111111_11111111_11111111;
			endcase
		end
		1'b0: begin
			case(column_i)
				3'd0: seg7_pins<=48'b11111111_11111111_11111111_11111111_11111111_10100011;
				3'd1: seg7_pins<=48'b11111111_11111111_11111111_11111111_10100011_11111111;
				3'd2: seg7_pins<=48'b11111111_11111111_11111111_10100011_11111111_11111111;
				3'd3: seg7_pins<=48'b11111111_11111111_10100011_11111111_11111111_11111111;
				3'd4: seg7_pins<=48'b11111111_10100011_11111111_11111111_11111111_11111111;
				3'd5: seg7_pins<=48'b10100011_11111111_11111111_11111111_11111111_11111111;
			endcase
		end
	endcase
end

endmodule